//////////////////////////////////////////////////////////////////////////
// CNPCPlayer default contructor & destructor
//////////////////////////////////////////////////////////////////////////

CNPCPlayer::CNPCPlayer()
{
	// variables
	m_dwID = 0;
	m_bAlive = false;

	// camera
	m_Camera = NULL;

	// models
	m_Head = NULL;
	m_Body = NULL;
	m_Legs = NULL;

	// qualities
	memset(&m_Qualities, 0, sizeof(m_Qualities));
}

