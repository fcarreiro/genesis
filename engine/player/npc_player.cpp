//////////////////////////////////////////////////////////////////////////
// Destroy() : Eliminates all player data from memory
//////////////////////////////////////////////////////////////////////////

void CNPCPlayer::Destroy()
{
	// destroy camera
	delete m_Camera;
	m_Camera = NULL;

	// destroy head model
	delete m_Head;
	m_Head = NULL;

	// destroy body model
	delete m_Body;
	m_Body = NULL;

	// destroy legs model
	delete m_Legs;
	m_Legs = NULL;
}

